function setActiveView(routeId) {
  if (routeId === "home") {
    posterHero.classList.remove("hidden-view");
    landingHero.classList.remove("hidden-view");
    summaryStrip.classList.remove("hidden-view");
    homeView.classList.add("active-view");
    docView.classList.remove("active-view");
    document.title = "庐山行程手册";
    return;
  }

  const doc = docs.find((item) => item.id === routeId) || docs[0];
  posterHero.classList.add("hidden-view");
  landingHero.classList.add("hidden-view");
  summaryStrip.classList.add("hidden-view");
  homeView.classList.remove("active-view");
  docView.classList.add("active-view");
  heroTitle.textContent = doc.title;
  heroSummary.textContent = doc.summary;
  docTag.textContent = doc.tag;
  docKicker.textContent = doc.kicker;
  renderStats(doc);
  documentContent.innerHTML = markdownToHtml(doc.content);
  document.title = `${doc.title} | 庐山行程手册`;
}

function updateUrl(routeId) {
  const nextHash = routeId === "home" ? "#home" : `#${routeId}`;
  if (window.location.hash !== nextHash) {
    window.history.pushState({ routeId }, "", nextHash);
  }
}

function renderRoute(routeId, options = {}) {
  const { syncUrl = true } = options;
  const nextRoute = routeId === "home" || docs.some((doc) => doc.id === routeId) ? routeId : "home";
  renderTopNav(nextRoute);
  setActiveView(nextRoute);
  if (syncUrl) {
    updateUrl(nextRoute);
  }
}

function closeDrawer() {
  mobileDrawer.classList.remove("open");
  menuToggle.setAttribute("aria-expanded", "false");
}

function syncActiveTab(routeId) {
  const activeTab = tabStrip.querySelector(`[data-route-link="${routeId}"]`);
  if (!activeTab) return;
  activeTab.scrollIntoView({ behavior: "smooth", inline: "center", block: "nearest" });
}

function scrollForRoute(routeId) {
  if (routeId === "home") {
    window.scrollTo({ top: 0, behavior: "smooth" });
    syncActiveTab(routeId);
    return;
  }

  requestAnimationFrame(() => {
    syncActiveTab(routeId);
    docView.scrollIntoView({ behavior: "smooth", block: "start" });
  });
}

document.addEventListener("click", (event) => {
  const link = event.target.closest("[data-route-link]");
  if (!link) return;
  event.preventDefault();
  const nextRoute = link.dataset.routeLink;
  renderRoute(nextRoute, { syncUrl: true });
  closeDrawer();
  scrollForRoute(nextRoute);
});

